//As variáveis podem ser escritas como var, let ou const caso a variável seja fixa

var a = 3
let b = 4

console.log(a, b)

// Variáveis que são declaradas como let não podem ser redeclaradas com o let na frente novamente 
var a = 30
b = 40

console.log(a, b)

//Variáveis constantes não podem ser mudadas no decoorrer do código

const c = 50

console.log(c)
